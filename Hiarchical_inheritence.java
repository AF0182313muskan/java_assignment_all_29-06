package Java_Practicals;

import java.util.Scanner;

public class Hiarchical_inheritence {
    static Scanner sc = new Scanner(System.in);
  static int num1,num2,res;
	
	
public static void add() {
	System.out.println("Enter two number ");
	num1 = sc.nextInt();
	num2 = sc.nextInt();
	res = num1+num2;
	System.out.println("the addition of two numbers = " +res);
	
}
public static void sub() {
	System.out.println("Enter two number ");
	num1 = sc.nextInt();
	num2 = sc.nextInt();
	res = num1-num2;
	System.out.println("the sub of two numbers = " +res);
	
}
public static void mul() {
	System.out.println("Enter two number ");
	num1 = sc.nextInt();
	num2 = sc.nextInt();
	res = num1*num2;
	System.out.println("the sub of two numbers = " +res);
	
}
public static void div() {
	System.out.println("Enter two number ");
	num1 = sc.nextInt();
	num2 = sc.nextInt();
	res = num1/num2;
	System.out.println("the sub of two numbers = " +res);
	
}
public static void mod() {
	System.out.println("Enter two number ");
	num1 = sc.nextInt();
	num2 = sc.nextInt();
	res = num1%num2;
	System.out.println("the sub of two numbers = " +res);
}
	
	
}
